    @staticmethod
    def _normalize_sql(sql: str) -> str:
        text = str(sql).strip()
        # Remove common code fences if a model accidentally included them.
        text = re.sub(r"^```(?:sql)?\s*", "", text, flags=re.IGNORECASE)
        text = re.sub(r"\s*```$", "", text)
        # Normalize smart quotes that may come from copy/paste.
        text = text.replace("‘", "'").replace("’", "'").replace("“", '"').replace("”", '"')
        # Trim trailing whitespace but keep SQL formatting for readable logs.
        lines = [line.rstrip() for line in text.splitlines()]
        return "\n".join(lines).strip()

    @staticmethod
    def _normalize_for_compare(sql: str) -> str:
        return re.sub(r"\s+", " ", sql.strip().rstrip(";")).upper()

    def _detect_status_only_sql(self, sql: str) -> str | None:
        if not self.config.allow_status_only_sql:
            return None
        match = re.fullmatch(
            r"\s*SELECT\s+'(DATA_GAP|ACCESS_DENIED|OUT_OF_SCOPE|NEEDS_CLARIFICATION|SQL_VALIDATION_FAILED)'\s+AS\s+status\s*;?\s*",
            sql,
            flags=re.IGNORECASE,
        )
        if match:
            return match.group(1).upper()
        normalized = self._normalize_for_compare(sql)
        for status_sql in self.status_sql_values:
            if normalized == self._normalize_for_compare(status_sql):
                status = self._detect_status_only_sql(status_sql)
                if status:
                    return status
        return None

    def _split_sql_statements(self, sql: str) -> list[str]:
        statements: list[str] = []
        start = 0
        in_single_quote = False
        i = 0
        while i < len(sql):
            ch = sql[i]
            if ch == "'":
                if in_single_quote and i + 1 < len(sql) and sql[i + 1] == "'":
                    i += 2
                    continue
                in_single_quote = not in_single_quote
            elif ch == ";" and not in_single_quote:
                part = sql[start:i].strip()
                if part:
                    statements.append(part)
                start = i + 1
            i += 1
        tail = sql[start:].strip()
        if tail:
            statements.append(tail)
        return statements

    @staticmethod
    def _strip_string_literals(sql: str) -> str:
        result: list[str] = []
        in_single_quote = False
        i = 0
        while i < len(sql):
            ch = sql[i]
            if ch == "'":
                result.append(" ")
                if in_single_quote and i + 1 < len(sql) and sql[i + 1] == "'":
                    result.append(" ")
                    i += 2
                    continue
                in_single_quote = not in_single_quote
                i += 1
                continue
            result.append(" " if in_single_quote else ch)
            i += 1
        return "".join(result)

    @staticmethod
    def _statement_type(sql: str) -> str:
        match = re.match(r"\s*([A-Za-z]+)", sql)
        return match.group(1).upper() if match else "UNKNOWN"

    def _has_top_level_final_select(self, sql: str) -> bool:
        positions = self._find_top_level_keyword_positions(sql, "SELECT")
        return bool(positions)

    @staticmethod
    def _find_top_level_keyword_positions(sql: str, keyword: str) -> list[int]:
        positions: list[int] = []
        upper = sql.upper()
        keyword_upper = keyword.upper()
        in_single_quote = False
        depth = 0
        i = 0
        while i < len(sql):
            ch = sql[i]
            if ch == "'":
                if in_single_quote and i + 1 < len(sql) and sql[i + 1] == "'":
                    i += 2
                    continue
                in_single_quote = not in_single_quote
                i += 1
                continue
            if not in_single_quote:
                if ch == "(":
                    depth += 1
                elif ch == ")" and depth > 0:
                    depth -= 1
                elif depth == 0 and upper.startswith(keyword_upper, i):
                    before = upper[i - 1] if i > 0 else " "
                    after = (
                        upper[i + len(keyword_upper)] if i + len(keyword_upper) < len(sql) else " "
                    )
                    if not (before.isalnum() or before == "_") and not (
                        after.isalnum() or after == "_"
                    ):
                        positions.append(i)
                        i += len(keyword_upper)
                        continue
            i += 1
        return positions

    def _extract_cte_names(self, sql: str) -> list[str]:
        if self._statement_type(sql) != "WITH":
            return []
        names: list[str] = []
        # Conservative extraction. It captures names in patterns like:
        # WITH department_level AS (...), other AS (...)
        for match in re.finditer(
            r"(?:\bWITH\b|,)\s*([A-Za-z_][A-Za-z0-9_]*)\s+AS\s*\(", sql, flags=re.IGNORECASE
        ):
            names.append(match.group(1))
        return list(dict.fromkeys(names))

    def _extract_relations(self, sql: str) -> list[JsonDict]:
        masked = self._strip_string_literals(sql)
        relations: list[JsonDict] = []
        for match in re.finditer(
            r"\bFROM\s+([A-Za-z_][A-Za-z0-9_]*(?:\.[A-Za-z_][A-Za-z0-9_]*)?)\s*(?:AS\s+)?([A-Za-z_][A-Za-z0-9_]*)?",
            masked,
            flags=re.IGNORECASE,
        ):
            relation_name = match.group(1)
            alias = match.group(2)
            # Avoid treating SQL clauses as aliases when no alias exists.
            if alias and alias.upper() in {
                "WHERE",
                "GROUP",
                "ORDER",
                "HAVING",
                "LIMIT",
                "UNION",
                "WINDOW",
            }:
                alias = None
            relations.append(
                {"name": relation_name, "alias": alias, "start": match.start(), "end": match.end()}
            )
        return relations

    def _extract_qualified_columns(self, sql: str) -> list[str]:
        masked = self._strip_string_literals(sql)
        alias = re.escape(self.config.required_alias)
        return [
            m.group(1)
            for m in re.finditer(
                rf"\b{alias}\s*\.\s*([A-Za-z_][A-Za-z0-9_]*)\b", masked, flags=re.IGNORECASE
            )
        ]

    def _extract_selected_expressions(self, sql: str) -> list[str]:
        select_positions = self._find_top_level_keyword_positions(sql, "SELECT")
        if not select_positions:
            return []
        select_pos = select_positions[-1]
        from_pos = self._find_next_top_level_keyword_position(
            sql, "FROM", start=select_pos + len("SELECT")
        )
        if from_pos is None:
            return []
        select_list = sql[select_pos + len("SELECT") : from_pos].strip()
        return self._split_top_level_commas(select_list)

    def _find_next_top_level_keyword_position(
        self, sql: str, keyword: str, *, start: int = 0
    ) -> int | None:
        positions = self._find_top_level_keyword_positions(sql[start:], keyword)
        if not positions:
            return None
        return start + positions[0]

    @staticmethod
    def _split_top_level_commas(text: str) -> list[str]:
        parts: list[str] = []
        start = 0
        depth = 0
        in_single_quote = False
        i = 0
        while i < len(text):
            ch = text[i]
            if ch == "'":
                if in_single_quote and i + 1 < len(text) and text[i + 1] == "'":
                    i += 2
                    continue
                in_single_quote = not in_single_quote
            elif not in_single_quote:
                if ch == "(":
                    depth += 1
                elif ch == ")" and depth > 0:
                    depth -= 1
                elif ch == "," and depth == 0:
                    part = text[start:i].strip()
                    if part:
                        parts.append(part)
                    start = i + 1
            i += 1
        tail = text[start:].strip()
        if tail:
            parts.append(tail)
        return parts

    def _extract_direct_selected_columns(self, selected_expressions: list[str]) -> list[str]:
        selected: list[str] = []
        for expression in selected_expressions:
            cleaned = re.sub(
                r"\s+AS\s+[A-Za-z_][A-Za-z0-9_]*\s*$", "", expression, flags=re.IGNORECASE
            ).strip()
            cleaned = re.sub(r"\s+[A-Za-z_][A-Za-z0-9_]*\s*$", "", cleaned).strip()
            match = re.fullmatch(
                r"(?:v\s*\.\s*)?([A-Za-z_][A-Za-z0-9_]*)", cleaned, flags=re.IGNORECASE
            )
            if match:
                selected.append(match.group(1))
        return selected

